# Basic print statement
print("Hello, DevOps Engineers!")

# Using different separator
print("Hello", "DevOps", "Engineers", sep="-")
print("Python", "is", "fun", sep="*")


# Adding end characters
print("Hello,", end=" ")
print("DevOps Engineers")


# Using formatted strings (f-strings)
name = "Harry"
print(f"Hello, {name}!") 


# Printing multiple lines
print("Hello\nDevops!")

#Experimenting with special characters
print("Hello,\tDevops") #tab character
print("Hello \"Devops\"!")  #double quotes inside the string

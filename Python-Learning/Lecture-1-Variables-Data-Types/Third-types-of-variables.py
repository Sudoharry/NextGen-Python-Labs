# Types of variables

# 1. String
name = "Harendra"

# 2. Integer
age = 29

# 3. Float
price = 10.5

# 4. Complex
x = 1j

# 5. List
fruits = ["apple", "banana", "cherry"]

# 6. Tuple
fruits = ("apple", "banana", "cherry")

# 7. Range
x = range(6)

# 8. Dictionary 
car =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}

# 9. Set
fruits = {"apple", "banana", "cherry"}

# 10. Frozen Set
fruits = frozenset({"apple", "banana", "cherry"})

# 11. Boolean
x = True

# 12. Bytes
x = b"Hello"

# 13. Byte Array
x = bytearray(5)

# 14. Memory View
x = memoryview(bytes(5))

# 15. None
x = None

# 16. Ellipsis
x = Ellipsis

# 17. Not a Number
x = float("nan")

# 18. Positive Infinity
x = float("inf")

# 19. Negative Infinity
x = float("-inf")

# 20. Complex Number
x = 1j  

# 21. Decimal
from decimal import Decimal
x = Decimal(10.5)   

# 22. Fraction
from fractions import Fraction
x = Fraction(1, 3)

# 23. Set
x = {1, 2, 3}


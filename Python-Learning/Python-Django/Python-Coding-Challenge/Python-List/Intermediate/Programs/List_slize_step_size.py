# List Slicing & Step size

# Slicing can be used to extract sublists in a flexible way.


nums = [10,20,30,40,50,60,70]

# Extracts the elements from index 1 to 4 
print(nums[1:5]) # Output: 20,30,40,50

# Get every 2nd element
print(nums[::2]) # Output: 10,30,50,70


# Reverse a list
print(nums[::-1])




